function triplejumpsplit(dt::T) where {T}
    c = T(2)^(1 // 3)
    c1 = 1 / (2(2 - c))
    c2 = (1 - c) / (2(2 - c))
    d1 = 1 / (2 - c)
    d2 = -c / (2 - c)
    return dt * [c1, d1, c2, d2, c2, d1, c1]
end

